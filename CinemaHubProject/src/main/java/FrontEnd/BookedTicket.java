    private void jTable1Contents(){
        DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
        ArrayList<Ticket> tickets = LoginPage.user.getTickets();
        for (Ticket ticket : tickets) {
            model.addRow(new Object[]{ticket.getForWhichMovie(),ticket.getSeatNumber(),ticket.getDate(),ticket.getID()});
        }
    }
    
